package main

import (
	"fmt"
	"os"

	"github.com/urfave/cli/v2"
)

func main() {
	app := &cli.App{
		Name:  "monapp",
		Usage: "Une application CLI simple",
		Action: func(c *cli.Context) error {
			fmt.Println("Bonjour, monde!")
			return nil
		},
	}

	err := app.Run(os.Args)
	if err != nil {
		fmt.Println(err)
		os.Exit(1)
	}
}
